package tiposvariaveis;

public class tipointeiro {
	public static void main(String[] args) {
		System.out.println("Trabalhando com tipo Inteiro: int");
		int idade;
		idade = 37;
		System.out.println("Isso aqui é um inteiro: "+idade);
		
		System.out.println("Trabalando uma TABUADA");
		int tabuada = 8;
		
		for(int i=0;i<=10;i++) {
			System.out.println(tabuada+" + "+i+" = "+tabuada * i);
		}
		
		}
	}