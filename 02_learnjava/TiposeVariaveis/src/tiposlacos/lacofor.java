package tiposlacos;

public class lacofor {
	public static void main(String[] args) {
		System.out.println("Informe. . .");
		int contador=0;
		
		//Imprimindo de 0 a 10
		for(int i=0;i<=10;i++) {
			System.out.println("O valor em FOR é "+i);
		}
		
		while (contador <=10) {
			System.out.println("Valor em WHILE é "+contador);
			contador++;
		}
	}
}
